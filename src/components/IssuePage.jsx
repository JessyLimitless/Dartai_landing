function IssueMarkdown({ content, colors, dark, tagColor }) {
  if (!content) return null
  const dim = dark ? '#4E5968' : '#8B95A1'
  const accentColor = tagColor || (dark ? '#6B7280' : '#9CA3AF')
  const lines = content.split('\n')
  const elements = []
  let tableRows = []
  let tableHeaders = []
  let inTable = false
  let inChart = false
  let chartTitle = ''
  let chartRows = []
  let key = 0

  const renderInline = (text) => {
    // **bold** + *italic*
    const parts = []
    let idx = 0, lastEnd = 0
    // Bold
    const re = /\*\*(.+?)\*\*/g
    let match
    while ((match = re.exec(text)) !== null) {
      if (match.index > lastEnd) parts.push(<span key={`t${idx++}`}>{text.slice(lastEnd, match.index)}</span>)
      parts.push(<strong key={`b${idx++}`} style={{ color: colors.textPrimary, fontWeight: 700 }}>{match[1]}</strong>)
      lastEnd = match.index + match[0].length
    }
    if (lastEnd < text.length) parts.push(<span key={`t${idx++}`}>{text.slice(lastEnd)}</span>)
    return parts.length > 0 ? parts : text
  }

  const flushTable = () => {
    if (tableHeaders.length > 0) {
      elements.push(
        <div key={`tbl-${key++}`} style={{ overflowX: 'auto', margin: '16px 0' }}>
          <table style={{ width: '100%', borderCollapse: 'collapse', fontSize: 13 }}>
            <thead>
              <tr>{tableHeaders.map((h, i) => (
                <th key={i} style={{
                  padding: '8px 10px', textAlign: 'left',
                  borderBottom: `1px solid ${dark ? '#333' : '#E4E4E7'}`,
                  color: dim, fontWeight: 600, fontSize: 11,
                }}>{renderInline(h)}</th>
              ))}</tr>
            </thead>
            <tbody>
              {tableRows.map((row, ri) => (
                <tr key={ri}>{row.map((cell, ci) => (
                  <td key={ci} style={{
                    padding: '8px 10px',
                    borderBottom: `1px solid ${dark ? '#222' : '#F0F0F2'}`,
                    color: colors.textSecondary, fontSize: 13,
                  }}>{renderInline(cell)}</td>
                ))}</tr>
              ))}
            </tbody>
          </table>
        </div>
      )
    }
    tableHeaders = []; tableRows = []; inTable = false
  }

  for (let i = 0; i < lines.length; i++) {
    const trimmed = lines[i].trim()

    // [CHART_H:title=...] 블록 시작
    if (trimmed.startsWith('[CHART_H')) {
      if (inTable) flushTable()
      inChart = true
      chartTitle = trimmed.match(/title=([^\]]+)/)?.[1] || ''
      chartRows = []
      continue
    }
    // 차트 블록 끝
    if (trimmed === '[/CHART_H]') {
      inChart = false
      const parsedRows = chartRows.map(line => {
        const parts = line.split(',')
        return { label: parts[0], value: parseFloat(parts[1]) || 0, win: parts[2] != null ? parseFloat(parts[2]) : null, n: parts[3] != null ? parseInt(parts[3]) : null }
      }).filter(r => r.label)
      if (parsedRows.length > 0) {
        elements.push(<HBarChart key={key++} rows={parsedRows} title={chartTitle} dark={dark} colors={colors} />)
      }
      chartRows = []
      continue
    }
    // 차트 데이터 라인 누적
    if (inChart) { if (trimmed) chartRows.push(trimmed); continue }

    // 테이블
    if (trimmed.startsWith('|') && trimmed.endsWith('|')) {
      const cells = trimmed.split('|').slice(1, -1).map(c => c.trim())
      if (cells.every(c => /^[-:]+$/.test(c))) continue
      if (!inTable) { tableHeaders = cells; inTable = true } else { tableRows.push(cells) }
      continue
    }
    if (inTable) flushTable()

    // 구분선
    if (trimmed === '---') {
      elements.push(<div key={key++} style={{ height: 1, background: dark ? 'rgba(255,255,255,0.04)' : 'rgba(0,0,0,0.04)', margin: '20px 0' }} />)
      continue
    }

    // 인용문
    if (trimmed.startsWith('> ')) {
      const quoteText = trimmed.replace(/^>\s*\*?/, '').replace(/\*$/, '')
      elements.push(
        <div key={key++} style={{
          padding: '12px 16px', margin: '12px 0', borderRadius: 12,
          borderLeft: `3px solid ${accentColor}`,
          background: dark ? `${accentColor}10` : `${accentColor}08`,
          fontSize: 13.5, color: colors.textSecondary, lineHeight: 1.75, fontStyle: 'italic',
        }}>{renderInline(quoteText)}</div>
      )
      continue
    }

    // H2
    if (trimmed.startsWith('## ')) {
      elements.push(<h2 key={key++} style={{
        fontSize: 17, fontWeight: 700, color: colors.textPrimary,
        margin: '24px 0 10px', letterSpacing: '-0.2px',
        borderBottom: `1px solid ${dark ? 'rgba(255,255,255,0.05)' : 'rgba(0,0,0,0.05)'}`,
        paddingBottom: 8,
      }}>{renderInline(trimmed.replace(/^##\s*/, ''))}</h2>)
      continue
    }

    // H3
    if (trimmed.startsWith('### ')) {
      elements.push(<h3 key={key++} style={{
        fontSize: 15, fontWeight: 700, color: colors.textPrimary,
        margin: '20px 0 8px',
      }}>{renderInline(trimmed.replace(/^###\s*/, ''))}</h3>)
      continue
    }

    // 리스트
    if (trimmed.startsWith('- ')) {
      elements.push(<div key={key++} style={{
        display: 'flex', gap: 8, margin: '4px 0', fontSize: 14,
        color: colors.textSecondary, lineHeight: 1.7,
      }}><span style={{ color: dim, flexShrink: 0 }}>·</span><span>{renderInline(trimmed.replace(/^-\s*/, ''))}</span></div>)
      continue
    }

    // 빈 줄
    if (!trimmed) { elements.push(<div key={key++} style={{ height: 8 }} />); continue }

    // 본문
    elements.push(<p key={key++} style={{
      margin: '0 0 8px', fontSize: 14.5, lineHeight: 1.8,
      color: colors.textSecondary, wordBreak: 'keep-all',
    }}>{renderInline(trimmed)}</p>)
  }
  if (inTable) flushTable()
  return <>{elements}</>
}
